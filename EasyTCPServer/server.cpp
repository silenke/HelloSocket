#include <iostream>
#include <thread>
#include "EasyTCPServer.hpp"

using namespace std;

bool g_bRun = true;
void cmdThread()
{
	while (true)
	{
		char cmdBuff[128]{};
		cin >> cmdBuff;
		if (!strcmp(cmdBuff, "exit")) {
			g_bRun = false;
			cout << "收到退出命令，结束任务！" << endl;
			return;
		}
		else {
			cout << "不支持的命令，请重新输入！" << endl;
		}
	}
}

int main()
{
	EasyTCPServer server;
	server.InitSocket();
	server.Bind(nullptr, 6100);
	server.Listen(5);

	// 启动UI线程
	thread t1(cmdThread);
	t1.detach();

	while (g_bRun)
	{
		server.OnRun();
	}

	server.Close();
	cout << "已退出！" << endl;

	Sleep(10000);
	return 0;
}
